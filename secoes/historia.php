                    <h2>Mundo</h2>
                    <p>O mundo de Red Dead Redemption 2 abrange cinco estados fictícios dos Estados Unidos. Os estados
                        de New Hanover, Ambarino e Lemoyne são novos para a série, e estão localizadas ao norte e leste
                        no mapa, enquanto os estados de New Austin e West Elizabeth retornam de Red Dead Redemption.
                        Os estados estão centralizados em San Luis e Lannahechee Rivers e nas margens de Flat Iron Lake.
                        Ambarino é um deserto de montanhas, com o maior assentamento sendo uma reserva de nativos
                        americanos; New Hanover é um vale amplo que se tornou um centro industrial e é o lar da cidade
                        pecuária de Valentine; e Lemoyne é composto por bayou e plantações semelhantes à Luisiana, e
                        abriga a cidade de Rhodes, no sul, e a antiga colônia francesa de Saint Denis, análoga a Nova
                        Orleans. West Elizabeth consiste em amplas planícies, florestas densas e a moderna cidade de
                        Blackwater. Esta região foi expandida a partir do primeiro Red Dead Redemption para incluir
                        uma vasta porção norte contendo a pequena cidade de Strawberry. New Austin é uma região
                        árida do deserto, centrada nas cidades fronteiriças de Armadillo e Tumbleweed, também presentes
                        no jogo original. Partes de New Austin e West Elizabeth foram redesenhadas para refletir tempos
                        mais antigos; por exemplo, Blackwater ainda está em construção, enquanto Armadillo se tornou uma
                        cidade fantasma por causa de um surto de cólera.</p>
