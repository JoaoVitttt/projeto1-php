                    <p>Enquanto um dos temas principais do primeiro jogo era proteger a família a todo custo, Red Dead
                        Redemption 2 conta a história do colapso de uma família na forma da gangue Van der Linde. Os
                        roteiristas estavam interessados em explorar a história de por que a gangue se desfez, como foi
                        algo frequentemente mencionado no primeiro jogo. Josh Bass, diretor de arte da Rockstar San
                        Diego, sentiu que a presença de Dutch (o líder da gangue Van der Linde) "pairava sobre o
                        primeiro jogo". Assim como seu antecessor, Red Dead Redemption 2 se passa durante a queda do
                        Velho Oeste, em um mundo que acabou se tornando moderno demais para a gangue. Aaron Garbut,
                        diretor de arte da Rockstar North, comentou que a Rockstar "pretendia capturar uma grande fatia
                        da vida americana em 1899, uma nação em rápida industrialização que logo estaria de olho no
                        cenário mundial". A equipe estava focada em criar uma reflexão fiel daquele período, tanto
                        nas pessoas quanto nos locais. Os cidadãos do jogo contrastam entre ricos e pobres, enquanto os
                        locais contrastam entre a civilização e o deserto.</p>

