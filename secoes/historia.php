                    <h2>Desenvolvimento</h2>
                    <p>Os trabalhos preliminares sobre Red Dead Redemption 2 começaram durante o desenvolvimento de Red
                        Dead Redemption (2010), enquanto a produção principal ocorreu após o lançamento de seu
                        antecessor, em maio de 2010. A Rockstar Games começou a receber pedidos internos para uma
                        sequência durante o desenvolvimento do primeiro jogo e achou a ideia de uma narrativa sobre
                        gangues "bastante atraente". O vice-presidente criativo Dan Houser falou com a Rockstar San
                        Diego — estúdio por trás do primeiro jogo — no início de 2011, sobre os personagens e o estilo
                        do jogo. A equipe teve um esboço aproximadamente até o meio do ano e, no final de 2012, muitos
                        scripts do jogo foram concluídos. Houser realizou videoconferências com diretores de
                        departamentos de todos os estúdios da Rockstar ao redor do mundo. Quando a Rockstar Games
                        percebeu que um grupo de estúdios distintos não necessariamente funcionaria, ela cooptou todos
                        os estúdios em uma grande equipe para facilitar o desenvolvimento entre 1.600 desenvolvedores;
